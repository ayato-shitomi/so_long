#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;
	t_list	*new_lst;

	if (!del)
		return ;
	new_lst = *lst;
	while (new_lst)
	{
		tmp = new_lst->next;
		del(new_lst->content);
		free(new_lst);
		new_lst = tmp;
	}
	*lst = NULL;
}
