#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	count;

	if (dst == NULL && src == NULL)
		return (0);
	if (size == 0)
		return (ft_strlen(src));
	count = 0;
	while (src[count] && count + 1 < size)
	{
		dst[count] = src[count];
		count++;
	}
	dst[count] = '\0';
	return (ft_strlen(src));
}
