#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*char_s;
	size_t			ch_len;

	char_s = (unsigned char *)s;
	ch_len = 0;
	while (ch_len < n)
	{
		if (char_s[ch_len] == (unsigned char)c)
			return ((void *)s + ch_len);
		else
			ch_len++;
	}
	return (NULL);
}
